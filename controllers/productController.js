// search product
export const searchProductController = async (req, res) => {
  try {
    const { keyword } = req.params;
    const results = await productModel
      .find({
        $or: [
          { name: { $regex: keyword, $options: "i" } }, //regex means regular expression and i means case insensitive
          { description: { $regex: keyword, $options: "i" } },
        ],
      })
      .select("-photo");
    res.json(results);
  } catch (error) {
    console.log(error);
    res.status(400).send({
      success: false,
      message: "Error In Search Product API",
      error,
    });
  }
};


//similar products based on company name and price range
export const relatedProductController = async (req, res) => {
  try {
    const { pid } = req.params;

    // Fetch the original product
    const originalProduct = await productModel.findById(pid).select("-photo");
    if (!originalProduct) {
      return res.status(404).send({
        success: false,
        message: "Original product not found",
      });
    }

    const { category, price, name } = originalProduct;

    // Extract "company" from first word of product name
    const company = name.split(" ")[0].toLowerCase();

    // Define price range (Â±20%)
    const priceRange = {
      $gte: price * 0.8,
      $lte: price * 1.2,
    };

    // Find related products with same company word and price range
    const relatedProducts = await productModel
      .find({
        _id: { $ne: pid },
        category,
        price: priceRange,
        name: { $regex: new RegExp(`^${company}`, "i") }, // starts with same company word
      })
      .select("-photo")
      .limit(8)
      .populate("category");

    res.status(200).send({
      success: true,
      products: relatedProducts,
    });
  } catch (error) {
    console.log(error);
    res.status(400).send({
      success: false,
      message: "Error while getting related products",
      error,
    });
  }
};


